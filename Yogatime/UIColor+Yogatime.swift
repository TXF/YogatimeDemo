import UIKit

extension UIColor {
    func ytsGreyishColor() -> UIColor {
        return UIColor.init(red: 144.0, green: 144.0, blue: 144.0, alpha: 1.0);
    }
    
    func ytsSandColor() -> UIColor {
        return UIColor.init(red: 232.0, green: 195.0, blue: 144.0, alpha: 1.0);
    }
}
